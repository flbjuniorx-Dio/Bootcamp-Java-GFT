package one.digitalinnovation.basecamp;

public class Calc {

    public static void soma(double number1, double number2) {

        double result = number1 + number2;

        System.out.println("Soma: " + number1 + " + " + number2 + " = " + result);

    }

    public static void subtracao(double number1, double number2) {

        double result = number1 - number2;

        System.out.println("Subtração: " + number1 + " - " + number2 + " = " + result);

    }

    public static void multiplicacao(double number1, double number2) {

        double result = number1 * number2;

        System.out.println("Multiplicação: " + number1 + " e " + number2 + " é igual a " + result);

    }

    public static void divisao(double number1, double number2) {

        double result = number1 - number2;

        System.out.println("A divisao de " + number1 + " e " + number2 + " é igual a " + result);

    }


}
